#include<bits/stdc++.h>
using namespace std;
int n,ans,st;
string s,obj;
int main(){
	freopen("reverse.in","r",stdin);
	freopen("reverse.out","w",stdout);
	cin>>n>>s;
	s = "." + s + ".";
	obj = s;
	reverse(obj.begin(),obj.end());
	if(obj == s){
		puts("0");
		return 0;
	}
	for(int i = n;i >= 1;--i){
		if(obj[i] == s[i])continue;
		int st = i;
		while(s[st] != obj[i])--st;
		for(int j = st;j < i;++j){
			swap(s[j],s[j+1]);
		}
		ans += (i-st);
	}
	cout<<ans;
	return 0;
}
