void EnqueueKokoro(){
	constexpr uint32_t unitMs = 140;
	constexpr uint32_t gapMs  = 5;
	
	for(const auto& entry : kLegacyScore) {
		const uint8_t tone = entry[0];
		if (tone == 37) break;            // STOP
		const NotePitch pitch = kToneMap[tone];
		const uint32_t duration = unitMs * entry[1];
		
		cout<<"Note(NotePitch::"<<kNotePitchName[static_cast<int32_t>(pitch)]<<","<<duration<<"),"<<endl;
		
		// 模仿原程序音符间 5ms 间隔
		if (gapMs != 0 && pitch != NotePitch::Rest) {
			cout<<"Note(NotePitch::Rest,5),"<<endl;
		}
	}
}

