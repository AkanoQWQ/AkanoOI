struct Note{
	NotePitch pitch;
	uint32_t duration;
	Note() = default;
	Note(NotePitch _pitch,uint32_t _duration){
		pitch = _pitch;
		duration = _duration;
	}
	float GetFrequencyHz() const {
		if(pitch == NotePitch::Rest) return 0.0f;
		if(pitch == NotePitch::End) return 0.0f;
		return static_cast<int32_t>(pitch) / 100.0f;
	}
};

